//do while
//If we want to execute loop body at least once then we should go for do-while. 

public class j_21_do_while {
    
    public static void main(String args[])
    {
        int a=10,b=20;
        do
        {
            System.out.println("hello");//hello
            
        }
        while(a>b);
        System.out.println("hi"); //hi
    }
}