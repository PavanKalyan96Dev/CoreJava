// FOR LOOP
//for loop: This is the most commonly used loop and best suitable if we know the no of iterations in advance.
public class j_22_for_loop {
    
    public static void main(String[] args) {

    int j=0;
    for(System.out.println("Hello world");j<3;j++)
    {
        System.out.println("Welcome to java " + j + " time");//Welcome to java

    }
    System.out.println("end of the loop j value is "+j);
        
    }
}
/* OUTPUT

Hello world
Welcome to java 0 time
Welcome to java 1 time
Welcome to java 2 time
end of the loop j value is 3

*/