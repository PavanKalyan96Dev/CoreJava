/*
Literals

Literals are fixed values (constants) that you directly write in your code.

👉 In simple words:
Literal = Actual value given to a variable

 */

public class j_3_Literals {
    public static void main(String[] args) {

          int a = 10;        // 10 → integer literal
    float b = 5.5f;    // 5.5 → float literal
    char c = 'A';      // 'A' → character literal
    String name = "Pavan";  // "Pavan" → string literal
    boolean flag = true;    // true → boolean literal

    System.out.println("Literals in Java:");
    System.out.println("Integer Literal: " + a);
    System.out.println("Float Literal: " + b);
    System.out.println("Character Literal: " + c);
    System.out.println("String Literal: " + name);
    System.out.println("Boolean Literal: " + flag);
        
    }
}

/* output

Literals in Java:
Integer Literal: 10
Float Literal: 5.5
Character Literal: A
String Literal: Pavan
Boolean Literal: true

 */




