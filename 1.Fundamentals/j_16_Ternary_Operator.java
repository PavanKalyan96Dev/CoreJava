// Ternary Operator
/* ternary operator 
The ternary operator is a shortcut for if-else condition in Java.
It is called ternary because it uses 3 operands.

*/
public class j_16_Ternary_Operator {

    public static void main(String args[]){

        int x=(10>20)?10:20;
        System.out.println(x); // Output: 20


    }
    
}